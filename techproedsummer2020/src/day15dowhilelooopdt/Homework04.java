package day15dowhilelooopdt;

import java.util.Scanner;

public class Homework04 {

	public static void main(String[] args) {
		/*
		 * Write a program to print letters from c to m on the console by using do-while
		 * loop.
		 */
		char ch = 'c';
		do {
			System.out.print(ch+" ");
			ch++;
		}while(ch<='m');

	}

}
