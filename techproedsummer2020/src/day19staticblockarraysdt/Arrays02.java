package day19staticblockarraysdt;

import java.util.Arrays;

public class Arrays02 {

	public static void main(String[] args) {
		
		
		String arr[]= {"Angie", "Leo", "Brad", "Adam", "Jack"};
		System.out.println(Arrays.toString(arr));
		
		
		// how to sort elements in ascending order
		Arrays.sort(arr);
		System.out.println(Arrays.toString(arr));
		
		
		//for numbers we use ascending order for string we use alphabetical order, they both are called natural order
	}

}
