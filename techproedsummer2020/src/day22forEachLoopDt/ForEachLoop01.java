package day22forEachLoopDt;

public class ForEachLoop01 {

	public static void main(String[] args) {
		// 1-) For each Loop works with collections (Array, List)
		// 2-) The other name of for each loop is enhanced loop
		
		int arr[]= {1, 2, 3, 4, 5};
		
		for (int w : arr) {
			System.out.print(w + " ");
		}
		
		

	}

}
